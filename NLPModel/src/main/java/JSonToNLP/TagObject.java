package JSonToNLP;

public class TagObject {
    private String tag;
    private String token;

    public String getTag() {
        return tag;
    }

    public String getToken() {
        return token;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public void setToken(String token) {
        this.token = token;
    }
}
